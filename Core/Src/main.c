void run_startup_phase(void) {
    HAL_ADC_Start(&hadc);
    HAL_ADC_PollForConversion(&hadc, 10);
    uint32_t pwr = HAL_ADC_GetValue(&hadc);
    HAL_ADC_Stop(&hadc);

    if (pwr >= PWR_MIN && pwr <= PWR_MAX) {
        HAL_UART_Transmit(&huart2, (uint8_t*)"[PIC] POWER OK\r\n", 16, 100);
        currentPhase = GENERAL_OP;
    } else {
        HAL_UART_Transmit(&huart2, (uint8_t*)"[PIC] POWER ERROR\r\n", 19, 100);
    }
}


void run_general_op_phase(void) {
    uint8_t cmd;
    if (HAL_UART_Receive(&huart2, &cmd, 1, 1) == HAL_OK) {
        switch(cmd) {
            case '1': // One-off
                currentMode = ONE_OFF;
                HAL_Delay(500);
                currentMode = STANDBY;
                break;
            case '2': // Continuous
                currentMode = CONTINUOUS;
                break;
            case '3': // Downlink
                savedMode = currentMode;
                currentMode = DOWNLINK;
                // Add downlink logic here later
                currentMode = savedMode; // Resume previous mode
                break;
        }
    }
}
